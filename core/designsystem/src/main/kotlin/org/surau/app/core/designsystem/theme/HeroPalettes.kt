/**
 * A named, hand-tuned Surau design-token palette. [SURAU_BASE] is the app default; [DEFAULT] keeps
 * the previous blue token set available as a selectable palette.
 */
enum class HeroPalette {
    SURAU_BASE,
    DEFAULT,
    MOUVE,
    SKY,
    MINT,
    DISCORD,
    UBER,
    AIRBNB,
}

private data class PaletteTokens(
    val name: String,
    val light: PaletteModeTokens,
    val dark: PaletteModeTokens,
)

