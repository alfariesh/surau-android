    val backgroundTertiary: Color,
)

private fun paletteModeTokens(
    background: Color,
    foreground: Color,
    muted: Color,
    overlay: Color,
    surface: Color,
    surfaceSecondary: Color,
    surfaceTertiary: Color,
    surfaceHover: Color,
    accent: Color,
    accentForeground: Color,
    accentSoft: Color,
    accentSoftForeground: Color,
    danger: Color,
    dangerForeground: Color,
    dangerSoft: Color,
    dangerSoftForeground: Color,
    success: Color,
    successForeground: Color,
    successSoft: Color,
    successSoftForeground: Color,
    warning: Color,
    warningForeground: Color,
    warningSoft: Color,
    warningSoftForeground: Color,
    border: Color,
    default: Color,
    scrollbar: Color,
    segment: Color,
    separator: Color,
    fieldBackground: Color,
    fieldBorder: Color,
    backgroundSecondary: Color,
    backgroundTertiary: Color,
): PaletteModeTokens {
    fun Color.opaque() = if (alpha < 1f) compositeOver(background).copy(alpha = 1f) else this
    return PaletteModeTokens(
        background = background.opaque(),
        foreground = foreground.opaque(),
        muted = muted.opaque(),
        overlay = overlay.opaque(),
        surface = surface.opaque(),
        surfaceSecondary = surfaceSecondary.opaque(),
        surfaceTertiary = surfaceTertiary.opaque(),
        surfaceHover = surfaceHover.opaque(),
        accent = accent.opaque(),
        accentForeground = accentForeground.opaque(),
        accentSoft = accentSoft.opaque(),
        accentSoftForeground = accentSoftForeground.opaque(),
        danger = danger.opaque(),
        dangerForeground = dangerForeground.opaque(),
        dangerSoft = dangerSoft.opaque(),
        dangerSoftForeground = dangerSoftForeground.opaque(),
        success = success.opaque(),
        successForeground = successForeground.opaque(),
        successSoft = successSoft.opaque(),
        successSoftForeground = successSoftForeground.opaque(),
        warning = warning.opaque(),
        warningForeground = warningForeground.opaque(),
        warningSoft = warningSoft.opaque(),
        warningSoftForeground = warningSoftForeground.opaque(),
        border = border.opaque(),
        default = default.opaque(),
        scrollbar = scrollbar.opaque(),
        segment = segment.opaque(),
        separator = separator.opaque(),
        fieldBackground = fieldBackground.opaque(),
        fieldBorder = fieldBorder.opaque(),
        backgroundSecondary = backgroundSecondary.opaque(),
        backgroundTertiary = backgroundTertiary.opaque(),
    )
}

private val HeroPalette.tokens: PaletteTokens
    get() = when (this) {
        HeroPalette.SURAU_BASE -> SurauBasePaletteTokens
        HeroPalette.DEFAULT -> DefaultPaletteTokens
        HeroPalette.MOUVE -> MouvePaletteTokens
        HeroPalette.SKY -> SkyPaletteTokens
        HeroPalette.MINT -> MintPaletteTokens
        HeroPalette.DISCORD -> DiscordPaletteTokens
        HeroPalette.UBER -> UberPaletteTokens
        HeroPalette.AIRBNB -> AirbnbPaletteTokens
    }

internal fun paletteName(palette: HeroPalette): String = palette.tokens.name

private fun PaletteModeTokens.semanticColors(): SurauSemanticColors = SurauSemanticColors(
    accent = accent,
    onAccent = accentForeground,
    accentSoft = accentSoft,
    default = default,
    background = background,
    surface = surface,
    surfaceSecondary = surfaceSecondary,
    surfaceTertiary = surfaceTertiary,
    surfaceHover = surfaceHover,
    overlay = overlay,
    fieldBackground = fieldBackground,
    fieldBorder = fieldBorder,
    border = border,
    separator = separator,
    muted = muted,
    scrollbar = scrollbar,
    segment = segment,
    success = success,
    onSuccess = successForeground,
    successContainer = successSoft,
    warning = warning,
    onWarning = warningForeground,
    warningContainer = warningSoft,
    danger = danger,
    onDanger = dangerForeground,
    dangerContainer = dangerSoft,
)

private fun PaletteModeTokens.colorScheme(dark: Boolean): ColorScheme {
    return if (dark) {
        darkColorScheme(
            primary = accent,
            onPrimary = accentForeground,
            primaryContainer = accentSoft,
            onPrimaryContainer = accentSoftForeground,
            inversePrimary = accent,
            secondary = accent,
            onSecondary = accentForeground,
            secondaryContainer = surfaceSecondary,
            onSecondaryContainer = foreground,
            tertiary = accent,
            onTertiary = accentForeground,
            tertiaryContainer = surfaceTertiary,
            onTertiaryContainer = foreground,
            error = danger,
            onError = dangerForeground,
            errorContainer = dangerSoft,
            onErrorContainer = dangerSoftForeground,
            background = background,
            onBackground = foreground,
            surface = surface,
            onSurface = foreground,
            surfaceVariant = surfaceSecondary,
            onSurfaceVariant = muted,
            surfaceContainerLowest = background,
            surfaceContainerLow = backgroundSecondary,
            surfaceContainer = surfaceSecondary,
            surfaceContainerHigh = surfaceTertiary,
            surfaceContainerHighest = separator,
            inverseSurface = foreground,
            inverseOnSurface = background,
            outline = border,
            outlineVariant = separator,
        )
    } else {
        lightColorScheme(
            primary = accent,
            onPrimary = accentForeground,
            primaryContainer = accentSoft,
            onPrimaryContainer = accentSoftForeground,
            inversePrimary = accent,
            secondary = accent,
            onSecondary = accentForeground,
            secondaryContainer = surfaceSecondary,
            onSecondaryContainer = foreground,
            tertiary = accent,
            onTertiary = accentForeground,
            tertiaryContainer = surfaceTertiary,
            onTertiaryContainer = foreground,
            error = danger,
            onError = dangerForeground,
            errorContainer = dangerSoft,
            onErrorContainer = dangerSoftForeground,
            background = background,
            onBackground = foreground,
            surface = surface,
            onSurface = foreground,
            surfaceVariant = surfaceSecondary,
            onSurfaceVariant = muted,
            surfaceContainerLowest = background,
            surfaceContainerLow = backgroundSecondary,
            surfaceContainer = surfaceSecondary,
            surfaceContainerHigh = surfaceTertiary,
            surfaceContainerHighest = separator,
            inverseSurface = foreground,
            inverseOnSurface = background,
            outline = border,
            outlineVariant = separator,
        )
    }
}

